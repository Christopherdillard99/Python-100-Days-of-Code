# import the modules and data needed for this project, along with ASCII art contained in files (same info posted above these lines):

import random

from replit import clear # this will be used to refresh the game if user guesses wrong and wants another try.
from art import logo, vs # use comma separator to import multiple variables from the art file.
from game_data import data # data is the list of dictionaries in the game_data file, containing account name, follower_count, description, and country as keys. 

# these are the functions that are critical to the games playability with explanations in the '''doc strings''':

def load_random_account():
  """Retrieves data from a random account in the 'data' list of dictionaries."""
  return random.choice(data)

def format_data(account):
  """Reformats accounts into a printable format with account name, description and country"""
  name = account["name"]
  description = account["description"]
  country = account["country"]
  # print(f'{name}: {account["follower_count"]}') could be used to reveal this information to the user upon a right guess, but that's too easy...
  return f"{name}, a {description}, from {country}"

def check_answer(guess, a_followers, b_followers):
  """Checks recorded value for follower count against user's guess 
  and returns True if they guessed correctly.
  Otherwise, False if they got it wrong.""" 
  if a_followers > b_followers:
    return guess == "a"
  else:
    return guess == "b"


def load_game():
  print(logo)
  score = 0 # later, a correct guess will add 1 to this default 0 score, otherwise the game ends. 
  continue_game = True
  account_a = load_random_account()
  account_b = load_random_account()

  while continue_game: #the colon placement here without a boolean or comparison operator in this while statement implicitly checks for a True condition for continue_game.
    account_a = account_b # in the next round, the account b replaces account a - this avoids a situation where a_random_account is instagram, which will always have more followers than anyone else which is no fun.
    account_b = load_random_account()

    while account_a == account_b: # this ensures that the next random account for b is not the same insta account as a.
      account_b = load_random_account()

    print(f"Compare A: {format_data(account_a)}.") # this prompt is standard with each round to ensure consistency, so correct indentation matters while nested here.
    print(vs)
    print(f"Against B: {format_data(account_b)}.")
    print("---------------------------------------------") # this is really just added to add some space and help with readability in the console for the user.
    
    user_guess = input("Who has more followers? Type 'A' or 'B': ").lower()
    a_follower_count = account_a["follower_count"]
    b_follower_count = account_b["follower_count"]
    is_correct = check_answer(user_guess, a_follower_count, b_follower_count)

    clear() # this way the console has a clean feel and new rounds down tag along after old ones... avoiding clutter.
    print(logo)
    if is_correct:
      score += 1
      print(f"Correctamundo! Current score: {score}.")
    else:
      game_should_continue = False
      print(f"Better luck next time! Final score: {score}")
      play_again = input("Play again? 'y' or 'n': ").lower()
      if play_again == "y":
        clear()
        load_game()

load_game()



